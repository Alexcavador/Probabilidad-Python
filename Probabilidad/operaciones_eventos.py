# Realizar algunas operaciones de conjuntos en Python

A = {0, 2, 4, 6, 8};
print ("A =", A);
B = {1, 2, 3, 4, 5};
print ("B =", B);

print("Unión :", A | B)
  
print("Intersección :", A & B)
  
print("Diferencia A - B:", A - B)

print("Diferencia B - A:", B - A)
  
print("Diferencia simétrica:", A ^ B)